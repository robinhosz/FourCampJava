package br.com.fourcamp.basico.main;

//Exemplo de atribuição de valores!

public class AtribuicaoValores {
	
	public static void main(String[] args) {
		
		int valor;
		
		
		System.out.println("---SOMA---");
		valor = 1;
		System.out.println("O valor atual é: "+ valor);
		valor = valor + 1;
		System.out.println("O valor atual é: "+ valor);
		valor = 1;
		System.out.println("O valor atual é: "+ valor);
		valor += 1;
		System.out.println("O valor atual é: "+ valor);
		valor = 1;
		System.out.println("O valor atual é: "+ valor);
		valor++;
		System.out.println("O valor atual é: "+ valor);
		
		System.out.println("---Subtração----");
			
		valor = 1;
		System.out.println("O valor atual é: "+ valor);
		valor = valor - 1;
		System.out.println("O valor atual é: "+ valor);
		valor = 1;
		System.out.println("O valor atual é: "+ valor);
		valor -= 1;
		System.out.println("O valor atual é: "+ valor);
		valor = 1;
		System.out.println("O valor atual é: "+ valor);
		valor--;
		System.out.println("O valor atual é: "+ valor);
		
		
	}

}
